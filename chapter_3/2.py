# For the following function calls, in what order are the subexpressions
# evaluated?

# a. min(max(3, 4), abs(-5))
print('min(max(3, 4), abs(-5)):', min(max(3, 4), abs(-5)))

# b. abs(min(4, 6, max(2, 8)))
print('abs(min(4, 6, max(2, 8))):', abs(min(4, 6, max(2, 8))))

# c. round(max(5.572, 3.258), abs(-2))
print('round(max(5.572, 3.258), abs(-2)):', round(max(5.572, 3.258), abs(-2)))