# Given variables x and y, which refer to values 3 and 12.5, respectively,
# use the print function to print the following messages. When numbers
# appear in the messages, variables x and y should be used.
x = 3
y = 12.5

# a. The rabbit is 3.
print('The rabbit is ', x, '.', sep='')

# b. The rabbit is 3 years old.
print('The rabbit is', x, 'years old.')

# c. 12.5 is average.
print(y, 'is average.')

# d. 12.5 * 3
print(y, '* 3')

# e. 12.5 * 3 is 37.5.
print(y, '*', x, 'is 37.5.')